// Public types mirroring the Python dataclasses.
// ─────────────────────────────────────────────────────────────────────────

/**
 * One admin slot's state.  Maps directly to the Chialisp record
 * ``(admin_idx leaves m_within)`` where ``leaves`` is the flat list
 * of member tree hashes representing the OneOfN of authentication
 * methods this admin can sign with, and ``m_within`` is the threshold
 * for KEY_REMOVE_QUORUM (typically 1 for single-method admins).
 */
export interface AdminRecord {
  /** 0-indexed admin slot identifier; immutable for the admin's lifetime. */
  adminIdx: number | bigint;
  /** 0x-prefixed 32-byte hex hashes of each MIPS member puzzle in this admin's OneOfN. */
  leaves: ReadonlyArray<string>;
  /** KEY_REMOVE_QUORUM threshold within this admin's leaves (1 ≤ m_within ≤ leaves.length). */
  mWithin: number | bigint;
}

/**
 * A pending key-rotation op awaiting activation or veto.  Maps to the
 * Chialisp record ``(admin_idx op_kind target_hash activates_at)``.
 */
export interface PendingOp {
  /** Which admin slot the op affects. */
  adminIdx: number | bigint;
  /** 1 = OP_KIND_ADD, 2 = OP_KIND_REMOVE.  Must match the puzzle constants. */
  opKind: number | bigint;
  /** 0x-prefixed 32-byte hex of the leaf being added or removed. */
  targetHash: string;
  /** Block height at which KEY_ADD_ACTIVATE / KEY_REMOVE_QUORUM becomes spendable. */
  activatesAt: number | bigint;
}

