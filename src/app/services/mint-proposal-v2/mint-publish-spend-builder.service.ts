// ── WASM SDK typing (narrowed) ────────────────────────────────────────
interface ProgramShape {
  treeHash(): Uint8Array;
  serialize(): Uint8Array;
  curry(args: ProgramShape[]): ProgramShape;
  uncurry?(): UncurriedProgramShape | null;
  toList?(): ProgramShape[] | null;
  toAtom?(): Uint8Array | null;
  toInt?(): bigint;
}
interface UncurriedProgramShape {
  program: ProgramShape;
  args: ProgramShape[] | ProgramShape;
}
interface ClvmShape {
  deserialize(bytes: Uint8Array): ProgramShape;
  atom(value: Uint8Array): ProgramShape;
  int(value: bigint): ProgramShape;
  list(value: ProgramShape[]): ProgramShape;
  pair(first: ProgramShape, rest: ProgramShape): ProgramShape;
  nil(): ProgramShape;
}
interface CoinClass {
  new (
    parentCoinInfo: Uint8Array,
    puzzleHash: Uint8Array,
    amount: bigint,
  ): {
    parentCoinInfo: Uint8Array;
    puzzleHash: Uint8Array;
    amount: bigint;
    coinId(): Uint8Array;
  };
}
interface PublishSdkShape {
  Clvm: new () => ClvmShape;
  Coin: CoinClass;
  Constants?: {
    singletonTopLayerV11?: () => Uint8Array;
    singletonTopLayer?: () => Uint8Array;
    singletonLauncher?: () => Uint8Array;
  };
}

interface ParsedPropertyRegistryInner {
  mod: ProgramShape;
  selfModHash: ProgramShape;
  govPubkey: ProgramShape;
  registeredIdsRoot: Uint8Array;
  registryVersion: bigint;
}
