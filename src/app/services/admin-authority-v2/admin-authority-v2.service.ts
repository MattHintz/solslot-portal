/**
 * Loose typing for the WASM Clvm class.  The full type lives in
 * ``chia_wallet_sdk_wasm.d.ts`` (~100 KB) and changes between minor
 * versions; we only need the few methods used here.
 */
interface ClvmShape {
  deserialize(bytes: Uint8Array): ProgramShape;
  atom(value: Uint8Array): ProgramShape;
  int(value: bigint): ProgramShape;
  list(values: ProgramShape[]): ProgramShape;
}

interface ProgramShape {
  treeHash(): Uint8Array;
  curry(args: ProgramShape[]): ProgramShape;
}

/**
 * Decode a 0x-prefixed (or bare) hex string to bytes.  Throws on
 * non-hex characters or odd length.
 */
function hexToBytes(hex: string): Uint8Array {
  let s = hex.startsWith('0x') || hex.startsWith('0X') ? hex.slice(2) : hex;
  if (s.length % 2 !== 0) {
    throw new Error(`hex string has odd length: ${hex}`);
  }
  const out = new Uint8Array(s.length / 2);
  for (let i = 0; i < out.length; i++) {
    const byte = parseInt(s.substring(i * 2, i * 2 + 2), 16);
    if (Number.isNaN(byte)) {
      throw new Error(`invalid hex at byte ${i}: ${hex}`);
    }
    out[i] = byte;
  }
  return out;
}

/**
 * Encode bytes as a 0x-prefixed lowercase hex string.  Stable output
 * for fixture comparisons.
 */
export function bytesToHexPrefixed(bytes: Uint8Array): string {
  let s = '0x';
  for (const b of bytes) {
    s += b.toString(16).padStart(2, '0');
  }
  return s;
}
